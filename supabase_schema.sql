-- Enable RLS and create broad policies to allow anon/authenticated access
alter table public."user" enable row level security;
create policy "allow_all_on_user" on public."user"
    for all
    to anon, authenticated
    using (true)
    with check (true);

alter table public.doctor enable row level security;
create policy "allow_all_on_doctor" on public.doctor
    for all
    to anon, authenticated
    using (true)
    with check (true);

alter table public.appointment enable row level security;
create policy "allow_all_on_appointment" on public.appointment
    for all
    to anon, authenticated
    using (true)
    with check (true);

alter table public.chat_history enable row level security;
create policy "allow_all_on_chat_history" on public.chat_history
