-- Supabase schema for Shewell
-- Run this in the Supabase SQL editor.

create table if not exists public."user" (
    id bigint generated by default as identity primary key,
    email text not null unique,
    password_hash text not null,
    name text not null,
    phone text not null,
    due_date date
);

create table if not exists public.doctor (
    id bigint generated by default as identity primary key,
    email text not null unique,
    password_hash text not null,
    name text not null,
    specialization text not null,
    experience integer not null,
    phone text not null,
    available_days text not null,
    per_minute_price numeric(10,2) not null default 0
);

create table if not exists public.appointment (
    id bigint generated by default as identity primary key,
    user_id bigint not null references public."user"(id) on delete cascade,
    doctor_id bigint not null references public.doctor(id) on delete cascade,
    date date not null,
    time text not null,
    status text not null default 'scheduled',
    created_at timestamptz not null default now()
);

create table if not exists public.chat_history (
    id bigint generated by default as identity primary key,
    user_id bigint not null references public."user"(id) on delete cascade,
    message text not null,
    response text not null,
    timestamp timestamptz not null default now()
);

create index if not exists idx_appointment_user_id on public.appointment(user_id);
create index if not exists idx_appointment_doctor_id on public.appointment(doctor_id);
create index if not exists idx_chat_history_user_id on public.chat_history(user_id);

grant usage on schema public to anon, authenticated;
grant select, insert, update, delete on table public."user" to anon, authenticated;
grant select, insert, update, delete on table public.doctor to anon, authenticated;
grant select, insert, update, delete on table public.appointment to anon, authenticated;
grant select, insert, update, delete on table public.chat_history to anon, authenticated;

